    // Implementation of DocumentListener --------------------------------------

    @Override
    public void changedUpdate(final DocumentEvent e) {
        updateTexts(e);
        if (txtProjectName.getDocument() == e.getDocument()) {
            firePropertyChange(
                PROP_PROJECT_NAME,
                null,
                txtProjectName.getText());
        }
    }

    @Override
    public void insertUpdate(final DocumentEvent e) {
        updateTexts(e);
        if (txtProjectName.getDocument() == e.getDocument()) {
            firePropertyChange(
                PROP_PROJECT_NAME,
                null,
                txtProjectName.getText());
        }
    }

    @Override
    public void removeUpdate(final DocumentEvent e) {
        updateTexts(e);
        if (txtProjectName.getDocument() == e.getDocument()) {
            firePropertyChange(
                PROP_PROJECT_NAME,
                null,
                txtProjectName.getText());
        }
    }

    /**
     * Handles changes in the Project name and project directory,.
     *
     * @param  e  DOCUMENT ME!
     */
    private void updateTexts(final DocumentEvent e) {
        final Document doc = e.getDocument();
        if ((doc == txtProjectName.getDocument())
                    || (doc == txtDistLocation.getDocument())) {
            // Change in the project name
            final String projectName = txtProjectName.getText();
            final String projectFolder = txtDistLocation.getText();
            final StringBuffer path = new StringBuffer();
            path.append(File.separatorChar).append("lib") // NOI18N
            .append(File.separatorChar).append("local")   // NOI18N
            .append(File.separatorChar).append("src")     // NOI18N
            .append(File.separatorChar).append("java")    // NOI18N
            .append(File.separatorChar);
            txtProjectFolder.setText(projectFolder + path.toString()
                + projectName);
        }
        panel.fireChangeEvent();                          // Notify that the panel changed
    }
