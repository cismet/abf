    /**
     * DOCUMENT ME!
     *
     * @param   wizardDescriptor  DOCUMENT ME!
     *
     * @return  DOCUMENT ME!
     */
    boolean valid(final WizardDescriptor wizardDescriptor) {
        if (txtProjectName.getText().length() == 0) {
            wizardDescriptor.putProperty(
                WizardDescriptor.PROP_ERROR_MESSAGE,
                org.openide.util.NbBundle.getMessage(
                    CidsJavaTemplatePanelVisual.class,
                    "CidsJavaTemplatePanelVisual.valid(WizardDescriptor).wizardDescriptor.invalidDirectoryNameProperty"));       // NOI18N
            return false;                              // Display name not specified
        }
        final File f = FileUtil.normalizeFile(new File(txtDistLocation.getText()).getAbsoluteFile());
        if (!f.isDirectory()) {
            wizardDescriptor.putProperty(
                WizardDescriptor.PROP_ERROR_MESSAGE,
                org.openide.util.NbBundle.getMessage(
                    CidsJavaTemplatePanelVisual.class,
                    "CidsJavaTemplatePanelVisual.valid(WizardDescriptor).wizardDescriptor.invalidProjectFolderProperty")); // NOI18N
            return false;
        }
        final File destFolder = FileUtil.normalizeFile(new File(txtProjectFolder.getText()).getAbsoluteFile());
        File projLoc = destFolder;
        while ((projLoc != null) && !projLoc.exists()) {
            projLoc = projLoc.getParentFile();
        }
        if ((projLoc == null) || !projLoc.canWrite()) {
            wizardDescriptor.putProperty(
                WizardDescriptor.PROP_ERROR_MESSAGE,
                org.openide.util.NbBundle.getMessage(
                    CidsJavaTemplatePanelVisual.class,
                    "CidsJavaTemplatePanelVisual.valid(WizardDescriptor).wizardDescriptor.projectFolderUncreatableProperty")); // NOI18N
            return false;
        }
        if (FileUtil.toFileObject(projLoc) == null) {
            wizardDescriptor.putProperty(
                WizardDescriptor.PROP_ERROR_MESSAGE,
                org.openide.util.NbBundle.getMessage(
                    CidsJavaTemplatePanelVisual.class,
                    "CidsJavaTemplatePanelVisual.valid(WizardDescriptor).wizardDescriptor.invalidProjectFolderProperty")); // NOI18N
            return false;
        }

        final File[] kids = destFolder.listFiles();
        if (destFolder.exists() && (kids != null) && (kids.length > 0)) {
            // Folder exists and is not empty
            wizardDescriptor.putProperty(
                WizardDescriptor.PROP_ERROR_MESSAGE,
                org.openide.util.NbBundle.getMessage(
                    CidsJavaTemplatePanelVisual.class,
                    "CidsJavaTemplatePanelVisual.valid(WizardDescriptor).wizardDescriptor.projectFolderExistsNonEmptyProperty"));                    // NOI18N
            return false;
        }
        wizardDescriptor.putProperty(WizardDescriptor.PROP_ERROR_MESSAGE, ""); // NOI18N
        return true;
    }

    /**
     * DOCUMENT ME!
     *
     * @param  d  DOCUMENT ME!
     */
    void store(final WizardDescriptor d) {
        final String name = txtProjectName.getText().trim();
        final String folder = txtProjectFolder.getText().trim();
        final File projDir = new File(folder);
        d.putProperty("projdir", projDir); // NOI18N
        d.putProperty("name", name);       // NOI18N
    }

    /**
     * DOCUMENT ME!
     *
     * @param  settings  DOCUMENT ME!
     */
    void read(final WizardDescriptor settings) {
        File distLocation = (File)settings.getProperty("projdir"); // NOI18N
        if (
            (distLocation == null)
                    || (distLocation.getParentFile() == null)
                    || !distLocation.getParentFile().isDirectory()) {
            distLocation = ProjectChooser.getProjectsFolder();
            // should be subdir of distribution
            if (
                distLocation.getName().equals("java")                            // NOI18N
                        && distLocation.getParentFile().getName().equals("src")) // NOI18N
            {
                distLocation = distLocation.getParentFile().getParentFile().getParentFile().getParentFile();
            }
        } else {
            // projDir should be subfolder of distribution
            distLocation = distLocation.getParentFile().getParentFile().getParentFile().getParentFile().getParentFile();
        }
        choosen = distLocation;
        txtDistLocation.setText(distLocation.getAbsolutePath());
        String projectName = (String)settings.getProperty("name"); // NOI18N
        if (projectName == null) {
            projectName = "cids Java Template";                    // NOI18N
        }
        txtProjectName.setText(projectName);
        txtProjectName.selectAll();
        panel.fireChangeEvent();
    }

